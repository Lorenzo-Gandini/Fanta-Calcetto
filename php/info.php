<h1 align="center"><strong>Regolamento</strong></b></h1>
<br/>

	<h4><b><i>Registrazione</i></b></h4>
	<p>Per accedere ai servizi è necessario registrarsi al sito. Sarà necessario fornire obbligatoriamente una mail valida, un nickname,
	e una password. Una volta effettuata la registrazione si sarà atumaticamente loggati dentro il sito.
	A destra, al posto di Log In comparirà il Logout per uscire dal sito. <br/>
  	Con esso compariranno le varie sezioni del sito: <i>Squadra, Mercato, Campionati, Profilo</i>.</p>
	<br/>

	<h4><b><i>Squadra</i></b></h4>
	<p> Qui puoi visionare la <b>tua squadra</b> attuale e <b>schierare le formazioni</b> per ogni campionato in cui sei iscritto dove la giornata è aperta.</p><br/>

	<h4><b><i>Mercato</i></b></h4>
	<p>Nella sezione "Mercato" è possibile effettuare la compravendita dei giocatori. Inizialmente si vendono i giocatori selezionati,
		poi se ne comprano altrettanti. Attenzione! Se vendi dei tuoi giocatori e poi non ne compri altri non potrai partecipare ai campionati nè
		schierare delle formazioni! "</p>
	<br/>

	 <h4><b><i>Campionati</i></b></h4>
	 <p>Su <b>I miei campionati</b> puoi vedere tutti i campionati in cui ti sei iscritto. Inoltre puoi iscriverti a tutti i campionati a cui ancora non partecipi
		 su <b>Iscrizione campionati</b>. Andando su Classifiche potrai selezionare il campionato desiderato e controllare la classifica giornaliera e generale.
 	In esporta classifiche potrai esportare in un file.xml le classifiche per ogni campionato, sia giornaliere che generali. </p><br/>



	 <h4><b><i>Profilo</i></b></h4>
	 <p>In questa sezione del sito potrai visionare tutte le informazioni del tuo profilo e modificarle.
		 inoltre puoi cancellare il profile, ma attento! non si può tornare più indietro e tutti i dati andranno persi.
   </p><br/>
